/**
 * Récupère un joueur aléatoire
 */
export function getRandomProPlayer(): ProPlayer {
  const randomIndex = Math.floor(Math.random() * proPlayers.length);
  return proPlayers[randomIndex];
}

/**
 * Récupère tous les joueurs
 */
export function getAllProPlayers(): ProPlayer[] {
  return proPlayers;
}

/**
 * Récupère un joueur par son ID
 */
export function getProPlayerById(id: string): ProPlayer | undefined {
  return proPlayers.find(player => player.id === id);
}

/**
 * Filtre les joueurs par région
 */
export function getProPlayersByRegion(region: string): ProPlayer[] {
  return proPlayers.filter(player => player.region === region);
}

/**
 * Filtre les joueurs par rôle
 */
export function getProPlayersByRole(role: string): ProPlayer[] {
  return proPlayers.filter(player => player.role === role);
}

/**
 * Récupère seulement les champions du monde
 */
export function getWorldChampions(): ProPlayer[] {
  return proPlayers.filter(player => player.hasWonWorlds);
}